  INFINITIVE TEXT,
  WORD_STRING TEXT,
  NORMALIZED_STRING TEXT,
  POS TEXT,
  LAST_PRACTICED_MS INTEGER,
  SKILL TEXT,
  SHORT_SKILL TEXT,
  LAST_PRACTICED TEXT,
  STRENGTH REAL,
  SKILL_URL_TITLE TEXT,
  GENDER TEXT,
  BOOKMARKED TEXT NOT NULL,
  COMPLETED TEXT NOT NULL,
  DELETED TEXT NOT NULL,
  SORT_ORDER INTEGER,
  CREATED_AT INTEGER NOT NULL,
  UPDATED_AT INTEGER NOT NULL
